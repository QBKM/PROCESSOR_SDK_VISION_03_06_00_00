void print_output()
{
   int i, j;

   printf(" OUTPUT DATA  \n");
   for (i = 0; i < OUT_H; ++i)
   {
      for (j = 0; j < OUT_W; ++j)
         printf("0x%2.2x, ", out1[i * OUT_W + j]);
      printf("\n");
   }
}

int verify_output()
{
   int ok = true;
   int i;

   for (i = 0; i < OUT_H * OUT_W; i++)
   {
      if (out1[i] != reference1[i])
      {
#ifdef CONSOLE
         printf("ERROR: \noutput1[%d] = %u\nreference1[%d] = %d\n",
                i, out1[i], i, reference1[i]);
#endif
         ok = false;
      }
   }
 #ifdef CONSOLE
   printf(ok ? "\n\nTransparentBlt Kernel-C code: PASS\n" : "FAIL\n\n");
#endif
return ok;
}

